s1 = "Hello"
s2 = 'Python'
s3 = """Welcome to string demo"""
s4 = s1 + " " + s2
print("Concatenation (s1 + ' ' + s2):", s4)

print("Repetition (s1 * 3):", s1*3)
sample = "   hello python world   "
print("Original sample: '", sample, "'", sep="")
print("upper()       ->", sample.upper())
print("lower()       ->", sample.lower())
words = sample.split()
print("split() ->", words)
